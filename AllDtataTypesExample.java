class AllDtataTypesExample{
	public static void main(String args[]){
		boolean flag=false;
			System.out.println("the boolean flag value is: " + flag);
		char letter='A';
			System.out.println("the char letter value is:" + letter);
		float Num=10.15f;
			System.out.println(" the float number value is: " + Num);
		double valNum=10.234567f;
			System.out.println(" the double value is: " + valNum);
		int intnum=3;
			System.out.println(" the integer number is: " + intnum);
		short shNum=111;
			System.out.println(" the short value number is: " + shNum);	
		byte byteval=6;
			System.out.println("the byte value is: " + byteval);
		long longval=10989;
			System.out.println("the long value is: " + longval);
	}
}
