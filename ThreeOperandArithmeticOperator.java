class ThreeOperandArithmeticOperator{
	public static void main(String args[]){
		int a1=2,a2=3,a3=6;
		int b1=12,b2=4,b3=7;
		int c1=3,c2=5,c3=6;
		int d1=50,d2=25,d3=5;
		int e1=39,e2=11,e3=2;
		 int sum=a1+a2+a3;
		 int sub=b1-b2-b3;
		 int mul=c1*c2*c3;
		 int div=d1/d2/d3;
		 int mod=e1%e2%e3;
			System.out.println("addition of three number variables is:" + sum);
			System.out.println("substraction of three number variables is:" + sub);
			System.out.println("multiple of three number variables is:" + mul);
			System.out.println("division of three number variables is:" + div);
			System.out.println("modulus  of three number variables is:" + mod);
	}
}