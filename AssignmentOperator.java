class AssignmentOperator{
	public static void main(String arg[]){
		System.out.println("program to illustrate assignment operators");
			int num=7;
			num+=10;
		System.out.println("the result of addition operator is: "+num);
		
			int num1=3;
			num1-=5;
		System.out.println("the result of subtraction operator is: "+num1);
		
			int num2=6;
			num2*=2;
		System.out.println("the result of multiplication operator is: "+num2);
		
			int num3=15;
			num3/=3;
		System.out.println("the result of division operator is: "+num3);
		
			int num4=30;
			num4%=3;
		System.out.println("the result of modulus operator is: "+num4);
	}
}
		
		
		