class DecrementUnaryOperator{
	public static void main(String args[]){
		System.out.println("program to illustrate decrement unary operator");
		int num=9;
		System.out.println("post decremernt of num: " +num--);
		System.out.println("after postdecrement of a number: " + num);
		System.out.println("pre decrement: " + --num);
		System.out.println("after predecrement of a number: " +num);
	}
}