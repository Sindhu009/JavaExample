class IncrementUnaryOperator{
	public static void main(String args[]){
		System.out.println("program to illustrate increment unary operator");
		int num=6;
		System.out.println("post increment of num: " + num);
		System.out.println("after postincrement of a number: " + num);
		System.out.println("preincrement: " + ++num);
		System.out.println("after preincrement of a number: " +num);
	}
}